package saijavafullstackdevelopement;

public class AssignmentOperator {
	
            public static void main(String[] args) {
            	
                int a=10;
                int b=5;
                
                System.out.println("value of a before operation:"+a);
                System.out.println("value of b before operation:"+b);
                a += 10;
                b += 4;
                a -= 10;
                b -= 3;
                a *= 2;
                b *= 2;
                a /= 2;
                b /= 2;
                
                System.out.println(" value of a after operation:" +a);
                System.out.println(" value of b after operation:" +b);
            }
}
